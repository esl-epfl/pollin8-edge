"""Turn results/metrics/*.csv into booktabs LaTeX tables.

Output is import-ready: \\input{tables/results_main.tex} from the paper. Keeping
tables in a separate folder lets the LaTeX be maintained without touching code.
"""
from __future__ import annotations
import argparse, csv
from pathlib import Path

METRICS = Path("results/metrics")


def _read(name):
    p = METRICS / name
    if not p.exists():
        return []
    rows = list(csv.DictReader(p.open()))
    # headers may carry stray spaces (see SHARED CONTRACT) -> strip keys.
    return [{(k.strip() if k else k): v for k, v in r.items()} for r in rows]


def _f(v):
    """Best-effort float; None on anything non-numeric/blank."""
    try:
        return float(str(v).strip())
    except (TypeError, ValueError):
        return None


DEPLOY_SIZE = 320          # deployed operating point: deliberate energy/accuracy choice


def deployed_size() -> int:
    """Deployed input size (px) = DEPLOY_SIZE (320).

    Final data has accuracy monotonic in resolution (argmax map50 is 512); we deploy at
    320 to roughly halve the inference energy and because all four loss-ablation arms were
    evaluated per species there (512 is the accuracy ceiling). Pinned so every table,
    figure and value agrees on the operating point."""
    return DEPLOY_SIZE


